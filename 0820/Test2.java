public class Test2 {
    public static void main (String [] args){
        // int variables
        int numOne = 5;
        int numTwo = 10;
        System.out.println(numOne + " " + numTwo);

        // float variables
        float floatOne = 5.0f;
        float floatTwo = 10.0f;
        System.out.println(floatOne + " " + floatTwo);

        // char variables
        char charOne = 'A';
        char charTwo = 'B';
        System.out.println(charOne + " " + charTwo);

        // double variables
        double doubleOne = 5.10;
        double doubleTwo = 10.5;
        System.out.println(doubleOne + " " + doubleTwo);

        // boolean variables
        boolean boolOne = true;
        boolean boolTwo = false;
        System.out.println(boolOne + " " + boolTwo);

        // result
        int resultOne = numOne + numTwo;
        System.out.println(resultOne);

        float resultTwo = floatOne + floatTwo;
        System.out.println(resultTwo);

        double resultThree = doubleOne + doubleTwo;
        System.out.println(resultThree);
    }
}
